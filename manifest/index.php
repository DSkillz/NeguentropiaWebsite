    <h2 style="text-decoration: underline; margin-top: 30px; width: 80%">Pour une science au
        service de TOUTES les formes de vie:</h2>
    <p>L’état de la recherche scientifique des civilisations humaines reflète avec une grande
        fidélité; ses comportements sociaux, la qualité de ses modes de vie -santé des
        individus-, ce qui en est produit, et le devenir de ses produits.</p>
    <p>Les connaissances humaines devraient être mises au service du vivant, non de la monnaie.
    </p>
    <p>Nous nous devons de compiler tout le savoir scientifique <span
            style="color: #00d900;">écocompatible</span> acquis jusqu’ici, et le mettre en
        sûreté pour les générations futures, sur des supports durables (également écocompatibles
        à long terme, via un effort de recherche).</p>
    <p>Un grand pouvoir implique de grandes responsabilités. Aussi les productions scientifiques
        et techniques doivent-elles toujours garantir la finalité éthique -sociale et
        environnementale- et tout mettre en œuvre pour ne produire rigoureusement aucune boîte
        de Pandore. Frankenstein, Orwell, Huxley et consorts ne doivent plus sortir des labos et
        des planches à dessin.</p>
    <p>À l’image du génie écologique encore balbutiant, les scientifiques, ingénieurs et
        techniciens doivent cesser de se penser en maîtres et possesseurs de la Nature,
        galvaudant la pensée de Descartes dans son Discours de la Méthode scientifique de 1637,
        pour enfin et définitivement se porter garants du Vivant -de TOUTES formes de vie sur
        Terre-, de l’accroissement qualitatif et quantitatif biodiversité/biomasse et de
        l’amélioration croissante de la qualité des équilibres biogéochimiques de Gaïa, par
        l’éthique environnementale, le principe de précaution, les études d’impacts, les
        solutions fondées sur la Nature.</p>
    <p>Pourtant dans l’Histoire des Sciences deux penseurs de génie systématiquement oubliés
        dans le fond d’un tiroir peuvent les y aider: Hippocrate, père de la médecine -« Face
        aux maladies, avoir deux choses à l’esprit : faire du bien, ou au moins ne pas faire de
        mal »- et plus proche de nous François Rabelais et son fameux « Sciences sans conscience
        n’est que ruine de l’âme ».</p>
    <p>Ne nous voilons plus la face: sans intégrité scientifique, sans éthique environnementale
        chevillées au corps tout au long d’une carrière de chercheur.se, d’ingénieur.e ou de
        technicien.ne, que valent 5, 8 ou plus années d’études, qu’est-ce qu’un diplôme obtenu
        par opportunisme et non par passion et volonté de « Primum non nocere », « D’abord ne
        pas nuire » sinon qu’un vulgaire bout de cellulose ? Combien de chercheurs.ses, de
        médecins pollueurs en SUV, censés lutter contre les maladies pulmonaires et les
        particules fines, combien d’ingénieur.e.s sur les plateformes pétrolières, de
        technicien.ne.s des eaux censés protéger cette précieuse ressource roulant ou volant au
        pétrole -une hérésie-, combien de chercheurs.ses en intelligence artificielle sur les
        armes autonomes létales ? Où est la logique mathématique chère à tout vrai bon
        scientifique dans tout ceci ?</p>
    <p>Pour finir, allons-nous continuer longtemps à produire des zettaoctets de productions
        scientifiques alors que le vivant tout entier est en passe d’être éradiqué ? N’est-ce
        pas absurde ?</p>
    <p>Il s’agit donc d’amorcer un virage majeur dans les savoirs et comportements, tout en
        améliorant la <span style="color: #00d900;">qualité de vie</span>, toujours dans un
        souci constant de<span style="color: #00d900;"> compatibilité harmonieuse avec le
            vivant</span>, même lorsqu’il est recouru à des sciences et/ou technologies
        avancées.</p>
    <p>Ainsi ces dernières devront <span style="color: #ff0000;">TOUJOURS</span> et <span
            style="color: #ff0000;">IMPERATIVEMENT</span> être soumises
        à<span style="color: #00d900;"> validation écotoxicologique rigoureuse</span>,
        et/ou<span style="color: #00d900;"> audit énergétique</span>, et/ou<span
            style="color: #00d900;"> analyses de cycle de vie.</span></p>
    <p>Toute Recherche Scientifique ou projet d’Ingénierie ou Technique contrevenant aux règles
        et préceptes mentionnés ci-dessus doit permettre l’exercice du <span
            style="color: #ff0000;">refus de parvenir</span> ou de la <span
            style="color: #ff0000;">censure</span>.</p>
